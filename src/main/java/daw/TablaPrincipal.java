    private void cargarDatos() {
        TablaPacientes modelo = new TablaPacientes();
        Object[] fila = new Object[modelo.getColumnCount()];
        for (int i = 0; i < lista.getLista().size(); i++) {
            fila[0] = lista.getLista().get(i).getId_paciente();
            fila[1] = lista.getLista().get(i).getNombre();
            fila[2] = lista.getLista().get(i).getFechaNacimiento();
            fila[3] = lista.getLista().get(i).getGrupoSanguineo();
            fila[4] = lista.getLista().get(i).getRh();
            fila[5] = lista.getLista().get(i).getNumeroDonaciones();
            modelo.addRow(fila);
        }
        jTable1.setModel(modelo);
    }

